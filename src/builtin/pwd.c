#include "../../include/minishell.h"

int mini_pwd(void)
{
	char *cwd;

	cwd = getcwd(NULL, 0);
	if (cwd)
	{
		printf("%s\n", cwd);
		free(cwd);
	}
	else
	{
		perror("getcwd");
		return(1);
	}
	return(0);
}
